    private void jBtnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnADDActionPerformed
        PerguntaDAO pdao = new PerguntaDAO();
        Pergunta p = new Pergunta();
        if (jTxtPergunta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencher todos os Campos!");
        } else {

            //Pega ID do item Selecionado na tabela
            int selecionado = (Integer.parseInt(jTablePerguntas.getValueAt(jTablePerguntas.getSelectedRow(), 0).toString()));

            p.setPergunta(jTxtPergunta.getText().toUpperCase());
            pdao.atualizar(p, selecionado);
            lerPerguntas();

            jTxtPergunta.setText("");

            esconderCampos();

            //Habilitar seleção de linhas novamente
            jTablePerguntas.setEnabled(true);
        }

    }//GEN-LAST:event_jBtnADDActionPerformed

    

    private void jBtnMaqUPDActionPerformed(java.awt.event.ActionEvent evt) {
        //LER OS DADOS DA LINHA SELECIOANDA
        if (jTablePerguntas.getSelectedRow() != -1) {
            //Travar a seleção de outra linha da tabela antes da alteração ser enviada
            jTablePerguntas.setEnabled(false);
            //Mostrar campos ocultos
            mostrarCampos();

            jTxtPergunta.setText(jTablePerguntas.getValueAt(jTablePerguntas.getSelectedRow(), 1).toString());

            op = 2;

        } else {
            JOptionPane.showMessageDialog(null, "Escolha uma Pergunta para alterar!");
        }
    }

