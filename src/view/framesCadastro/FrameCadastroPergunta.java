    public void lerPerguntas() {
        DefaultTableModel modelo = (DefaultTableModel) jTablePerguntas.getModel();
        JTableUtilities.centralizarHeader(jTablePerguntas);
        JTableUtilities.alinharColuna(jTablePerguntas, SwingConstants.CENTER, 0);
        PerguntaDAO p = new PerguntaDAO();
        modelo.setNumRows(0);

        for (Pergunta i : p.listar()) {

            modelo.addRow(new Object[]{
                i.getId(),
                i.getPergunta()
            });
        }
    }

